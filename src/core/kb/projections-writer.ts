/**
 * KB projections WRITER (Phase 5) — the IO runner that turns the pure
 * projections (projections.ts) into the on-disk files the session-start
 * injection reads: persona.md + scene_blocks/*.md + .metadata/scene_index.json.
 *
 * This module is intentionally separated from projections.ts so that the
 * rendering stays PURE + unit-testable (no fs), while all filesystem effects +
 * the scene-navigation wiring live here. It is the single, reusable entry point
 * used by both the live pipeline refresh and the one-shot CLI (tools/kb-project).
 *
 * Format contracts (so NO hook change is needed):
 *   - persona.md = persona body (deterministic projection) + a trailing
 *     scene-navigation section generated by generateSceneNavigation() — exactly
 *     the shape the <user-persona> / <scene-navigation> injection expects.
 *   - scene_blocks/<name>.md = META-delimited scene block; .metadata/scene_index.json
 *     is written DIRECTLY from the projection's index (identical to what
 *     syncSceneIndex would parse back out of the META headers).
 *
 * Safety:
 *   - Scene blocks are written into scene_blocks/ ONLY; the writer refuses to
 *     touch any path outside that directory (filenames are slugified +
 *     basename-checked).
 *   - Stale scene blocks from a previous projection run that are no longer
 *     produced are removed (the projection is the source of truth), but ONLY
 *     files that look like projector output ("scene-*.md") are eligible for
 *     removal — hand-authored / legacy scene files are left untouched.
 */

import fs from "node:fs/promises";
import path from "node:path";
import { generateSceneNavigation } from "../scene/scene-navigation.js";
import { writeSceneIndex } from "../scene/scene-index.js";
import type { SceneIndexEntry } from "../scene/scene-index.js";
import {
  projectPersonaBody,
  projectScenes,
  type ProjectionStore,
  type ProjectionOptions,
  type ProjectedScene,
} from "./projections.js";

const TAG = "[memory-tdai][kb-projections]";

interface ProjectionWriterLogger {
  debug?: (message: string) => void;
  info: (message: string) => void;
  warn: (message: string) => void;
  error: (message: string) => void;
}

export interface ProjectAllOptions extends ProjectionOptions {
  /** Absolute path to the plugin data directory (persona.md + scene_blocks/ live here). */
  dataDir: string;
  /** Max recent events to scan when grouping scenes (default 200). */
  sceneEventLimit?: number;
  /** Keep only the top-N scenes by heat so the session-start injection stays focused (default 12). */
  maxScenes?: number;
  logger?: ProjectionWriterLogger;
}

export interface ProjectAllResult {
  /** Absolute path to the written persona.md. */
  personaPath: string;
  /** Number of scene block files written. */
  scenesWritten: number;
  /** Number of stale projector scene files removed. */
  scenesRemoved: number;
}

/** Filenames produced by the scene projector all share this prefix. */
const SCENE_PREFIX = "scene-";

/**
 * Regenerate persona.md + scene_blocks/* + scene_index.json from the KB.
 *
 * Steps (deterministic, NO LLM):
 *   1. project scenes → write each scene_blocks/<name>.md (verbatim block).
 *   2. remove stale projector scene files (scene-*.md) no longer produced.
 *   3. write .metadata/scene_index.json from the projection index.
 *   4. project persona body → append fresh scene navigation → write persona.md.
 */
export async function projectAll(
  store: ProjectionStore,
  opts: ProjectAllOptions,
): Promise<ProjectAllResult> {
  const { dataDir, logger } = opts;
  const namespace = opts.namespace?.trim() || "default";
  const sceneBlocksDir = path.join(dataDir, "scene_blocks");
  const personaPath = path.join(dataDir, "persona.md");

  await fs.mkdir(sceneBlocksDir, { recursive: true });
  await fs.mkdir(path.join(dataDir, ".metadata"), { recursive: true });

  // ── 1. Scenes — cap to the top-N by heat so the session-start injection stays
  //    focused (one scene per entity would otherwise inject dozens of lines). ──
  const maxScenes = opts.maxScenes ?? 12;
  const { scenes: allScenes } = projectScenes(store, { namespace, locale: opts.locale, limit: opts.sceneEventLimit });
  const scenes = [...allScenes].sort((a, b) => b.index.heat - a.index.heat).slice(0, maxScenes);
  const produced = new Set<string>();
  for (const scene of scenes) {
    const safe = assertSceneFilename(scene.filename);
    await fs.writeFile(path.join(sceneBlocksDir, safe), scene.content, "utf-8");
    produced.add(safe);
  }
  logger?.debug?.(`${TAG} wrote ${scenes.length} scene block(s)`);

  // ── 2. Remove stale projector scene files (scene-*.md only) ──
  const scenesRemoved = await removeStaleProjectorScenes(sceneBlocksDir, produced, logger);

  // ── 3. Scene index (written directly from the projection index) ──
  const indexEntries: SceneIndexEntry[] = scenes.map((s) => s.index);
  await writeSceneIndex(dataDir, indexEntries);

  // ── 4. Persona body + scene navigation → persona.md ──
  const body = projectPersonaBody(store, { namespace, locale: opts.locale });
  const nav = generateSceneNavigation(indexEntries);
  const finalContent = nav ? `${body}\n\n${nav}\n` : `${body}\n`;
  await fs.writeFile(personaPath, finalContent, "utf-8");
  logger?.info(
    `${TAG} projected persona.md (${finalContent.length} chars) + ${scenes.length} scene(s), removed ${scenesRemoved} stale`,
  );

  return { personaPath, scenesWritten: scenes.length, scenesRemoved };
}

/**
 * Validate a projector-produced scene filename: it MUST be a bare basename (no
 * path separators, no traversal) under scene_blocks/. Throws otherwise — a
 * deterministic projector should never produce an unsafe name, so this is a
 * defensive invariant, not an expected branch.
 */
function assertSceneFilename(filename: string): string {
  const base = path.basename(filename);
  if (base !== filename || base.includes("..") || base.length === 0) {
    throw new Error(`${TAG} refusing unsafe scene filename: ${JSON.stringify(filename)}`);
  }
  return base;
}

/**
 * Delete projector scene files (scene-*.md) that the CURRENT projection did not
 * produce. Hand-authored / legacy scene files (any name NOT starting with
 * "scene-") are left untouched — the projector only owns its own output.
 */
async function removeStaleProjectorScenes(
  sceneBlocksDir: string,
  produced: Set<string>,
  logger?: ProjectionWriterLogger,
): Promise<number> {
  let removed = 0;
  let existing: string[];
  try {
    existing = (await fs.readdir(sceneBlocksDir)).filter((f) => f.endsWith(".md"));
  } catch {
    return 0;
  }
  for (const file of existing) {
    if (!file.startsWith(SCENE_PREFIX)) continue; // not projector output → keep
    if (produced.has(file)) continue; // still produced → keep
    try {
      await fs.unlink(path.join(sceneBlocksDir, file));
      removed++;
      logger?.debug?.(`${TAG} removed stale projector scene: ${file}`);
    } catch (err) {
      logger?.warn(`${TAG} failed to remove stale scene ${file}: ${err instanceof Error ? err.message : String(err)}`);
    }
  }
  return removed;
}

/** Re-export so callers can render scenes without importing two modules. */
export type { ProjectedScene };
